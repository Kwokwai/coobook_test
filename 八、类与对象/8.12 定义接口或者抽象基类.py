from abc import ABCMeta, abstractmethod


class IStream(metaclass=ABCMeta):
    @abstractmethod
    def read(self, maxbytes=-1):
        pass

    @abstractmethod
    def write(self, data):
        pass


a = IStream()


class StocketStream(IStream):
    def read(self, maxbyes=-1):
        pass

    def write(self, data):
        pass



